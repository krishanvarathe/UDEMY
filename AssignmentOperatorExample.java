public class AssignmentOperatorExample {
    
    public static void main(String[] args) {
        int x = 10;
        x += 5;
        System.out.println("values : "+x);
        x -= 5;
        System.out.println("values : "+x);
        x *= 5;
        System.out.println("values : "+x);
        x /= 5;
        System.out.println("values : "+x);
        x %= 5;
        System.out.println("values : "+x);
    }
    
}
