public class RelationalOperator {
    static int a=10;
    static int b=20;
    public static void main(String[] args) {
        
        System.out.println("a == b :"+(a==b));
        System.out.println("a != b :"+(a!=b));
        System.out.println("a > b :"+(a>b));
        System.out.println("a < b :"+(a<b));
        System.out.println("a >= b :"+(a>=b));
        System.out.println("a <= b :"+(a<=b));
        
    }
    
}
